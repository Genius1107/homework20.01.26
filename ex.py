# Программа проверяет, "счастливый" ли билет (шестизначная строка)
s = input()                  # читаем строку от пользователя
if len(s) != 6:              # проверяем, что длина равна 6
    print("Неверный ввод")   # если нет — сообщаем об ошибке
else:
    # проверяем, что все символы — цифры
    ok = True
    for ch in s:
        if ch < '0' or ch > '9':
            ok = False
            break
    if not ok:
        print("Неверный ввод")
    else:
        # берём первые три и последние три символа
        a = s[0] + s[1] + s[2]
        b = s[3] + s[4] + s[5]
        # преобразуем символы в числа и суммируем
        s1 = int(a[0]) + int(a[1]) + int(a[2])
        s2 = int(b[0]) + int(b[1]) + int(b[2])
        # сравниваем суммы и выводим результат
        if s1 == s2:
            print("Счастливый")
        else:          
            print("Обычный")
