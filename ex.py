# Программа проверяет, можно ли отломить k долек от шоколадки n × m одним разломом
# (разлом должен быть по сетке между дольками, то есть получается два прямоугольника)

# читаем три числа (n, m, k) от пользователя через пробел
s = input()               # пример ввода: "3 2 4"
parts = s.split()         # разобьём строку по пробелам

# проверим, что ввёлось ровно три части
if len(parts) != 3:
    print("Неверный ввод")
else:
    # попробуем преобразовать в целые числа
    ok = True
    try:
        n = int(parts[0])
        m = int(parts[1])
        k = int(parts[2])
    except:
        ok = False

    if not ok:
        print("Неверный ввод")
    else:
        # проверим, что размеры положительные и k неотрицательное
        if n <= 0 or m <= 0 or k < 0:
            print("Неверный ввод")
        else:
            total = n * m
            # если хотим отломить ровно все или 0 долек, то это возможно только если k != 0 и k != total?
            # на самом деле разрешается отломить k долек, к примеру k = total — тогда не будет второго прямоугольника,
            # обычно считают, что отломить все — нельзя, потому что нужен разлом. Но чаще принято считать:
            # можно отломить k, если k < total и (k кратно n или k кратно m)
            if k == 0 or k >= total:
                print("Нет")
            else:
                # проверяем, можно ли получить кусок размером k, сделав один прямой разлом:
                # это возможно, если k делится на n (тогда отломается полоска по m) или на m
                if (k % n == 0) or (k % m == 0):
                    print("Да")
                else:
                    print("Нет")



